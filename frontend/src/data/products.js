export const allProducts = rawCatalog.map((p, i) => ({
  id: i + 1,
  slug: slugify(p.name),
  ...p,
  priceLabel: `₹${p.price}`,
  oldPriceLabel: p.oldPrice ? `₹${p.oldPrice}` : null,
}))

export function getProductBySlug(slug) {
  return allProducts.find(p => p.slug === slug)
}

export function getRelatedProducts(product, count = 4) {
  if (!product) return allProducts.slice(0, count)
  return allProducts
    .filter(p => p.category === product.category && p.id !== product.id)
    .slice(0, count)
    .concat(allProducts.filter(p => p.id !== product.id))
    .slice(0, count)
}